from backend.domain.models import ReportRequest


class ReportPromptBuilder:
    """Builds a stable prompt for an open-source LLM running through Ollama."""

    def build(self, data: ReportRequest) -> str:
        prompt = (
            "Ты — помощник для создания подробных отчётов на русском языке. "
            "Сформируй comprehensive report: развёрнутый, логичный, структурированный и пригодный для сдачи. "
            "Используй только локальные данные, которые передал пользователь. "
            "Не выдумывай факты, числа, ссылки, результаты экспериментов или названия файлов.\n\n"
            "Локальные данные пользователя:\n"
            f"1. Цель / задача: {data.goal}\n"
            f"2. Ход работы / исходные материалы: {data.process}\n"
            f"3. Результаты: {data.results or 'Не указаны отдельно'}\n"
            f"4. Вывод пользователя: {data.conclusion}\n\n"
        )

        if data.tone:
            prompt += f"Пожелания по стилю и тону: {data.tone}\n\n"

        prompt += (
            "Сделай отчёт с разделами: Введение, Цель работы, Исходные данные, "
            "Ход работы, Результаты, Анализ, Заключение. "
            "Пиши связным академическим стилем. "
            "Если данных недостаточно, аккуратно укажи, что конкретный раздел требует уточнения."
        )
        return prompt
